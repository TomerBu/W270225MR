
# Lists and Tuples
my_list = [1, 2, 3]
my_tuple = (1, 2, 3)

# Accessing Elements
print(my_list[0])  # Output: 1
print(my_tuple[0])  # Output: 1

# Modifying Elements
my_list[0] = 10
# my_tuple[0] = 10  # This will raise an error

# Length
print(len(my_list))  # Output: 3
print(len(my_tuple))  # Output: 3

# Iterating
for item in my_list:
    print(item)

for item in my_tuple:
    print(item)